# .strip(): mehtod which removes all of the leading leading and trailing spaces from a string
text= input('enter a text: ')
print(text.strip())

#  len(): function which tells the length of string
print(len(text))
print(len(text.strip()))

#  .lower(): method which turns everything to lowercase in a string
print(text.lower())

#  .upper(): method which turns everything to uppercase in a string
print(text.upper())

#  .split(): method which splits up the text on basis of a deluminator(space by default) in the form of a List
print(text.split())
