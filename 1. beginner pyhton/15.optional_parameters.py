# Optional parameters
def func(x,text = '2'): # here 'text' is an optional parameter whose default value is '2'
    # Note: always put the optional parametres at the last in bracket
    print(x)
    if text == '1':
        print('Text is 1')
    else:
        print('Text is not 1')

func('vt','1')
func('tandon') 
print(6-5.7)